
<script>
import { Bar } from "vue-chartjs";
import { numbers, colors } from "../utils/randomUtils";

// global constants
const MONTHS = 12;
const mColors = colors(MONTHS);
const mNumbers = numbers(MONTHS);

export default {
  extends: Bar,
  data: () => ({
    chartdata: {
      labels: [
        "January",
        "February",
        "March",
        "April",
        "May",
        "June",
        "July",
        "August",
        "September",
        "October",
        "November",
        "December"
      ],
      datasets: [
        {
          backgroundColor: mColors,
          data: mNumbers
        }
      ]
    },
    options: {
      responsive: true,
      maintainAspectRatio: false,
      legend: {
        display: false
      },
      scales: {
        yAxes: [
          {
            ticks: {
              beginAtZero: true
            }
          }
        ]
      }
    }
  }),

  mounted() {
    this.renderChart(this.chartdata, this.options);
  }
};
</script>
